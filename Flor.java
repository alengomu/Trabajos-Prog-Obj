public class Flor {
    private String actitud;
    private String apariencia;
//crtl + H cambias todas las palabras a la vez

    public Flor(String actitud, String apariencia){
        this.actitud = actitud;
        this.apariencia = apariencia;

    }
    public String getActitud() { //doy
        return this.actitud;

    }
    public void setActitud (String actitud){ //dame
        this.actitud = actitud;
    }    
    
    public String getApariencia() { //doy
        return this.apariencia;

    }
    public void setApariencia (String apariencia){ //dame
        this.apariencia = apariencia;
}
public Flor() {
   
}
}
